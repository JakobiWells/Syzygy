function formatTransitDuration(ms) {
  if (!ms) return '<1s'
  const s = ms / 1000
  if (s < 1) return `${(s * 1000).toFixed(0)}ms`
  return `${s.toFixed(1)}s`
}

const TWO_WEEKS_MS = 14 * 24 * 3600 * 1000

function TransitDiscIcon({ transit }) {
  const canvasRef = useRef(null)

  const draw = useCallback(() => {
    const canvas = canvasRef.current
    if (!canvas) return
    const ctx = canvas.getContext('2d')
    const W = canvas.width, H = canvas.height
    const cx = W / 2, cy = H / 2
    const R = W * 0.42

    ctx.clearRect(0, 0, W, H)

    // Disc fill + border
    ctx.beginPath()
    ctx.arc(cx, cy, R, 0, Math.PI * 2)
    if (transit.type === 'solar') {
      ctx.fillStyle = '#fef9c3'
      ctx.fill()
      ctx.strokeStyle = '#ca8a04'
    } else {
      // Moon: add a subtle gradient for depth
      const grad = ctx.createRadialGradient(cx - R * 0.25, cy - R * 0.2, R * 0.05, cx, cy, R)
      grad.addColorStop(0, '#f8fafc')
      grad.addColorStop(1, '#cbd5e1')
      ctx.fillStyle = grad
      ctx.fill()
      ctx.strokeStyle = '#94a3b8'
    }
    ctx.lineWidth = 1.5
    ctx.stroke()

    // ISS path across the disc
    const pts = transit.discPath
    if (!pts || pts.length < 2) return

    // Dashed extension lines entering/leaving the disc
    const first = pts[0], last = pts[pts.length - 1]
    const dx = last.x - first.x, dy = last.y - first.y
    const dlen = Math.sqrt(dx * dx + dy * dy)
    if (dlen > 0) {
      const ex = dx / dlen * 0.35, ey = dy / dlen * 0.35
      ctx.save()
      ctx.setLineDash([2, 2])
      ctx.strokeStyle = transit.type === 'solar' ? 'rgba(0,0,0,0.25)' : 'rgba(0,0,0,0.2)'
      ctx.lineWidth = 1.2
      ctx.beginPath()
      ctx.moveTo(cx + (first.x - ex) * R, cy - (first.y - ey) * R)
      ctx.lineTo(cx + first.x * R, cy - first.y * R)
      ctx.stroke()
      ctx.beginPath()
      ctx.moveTo(cx + last.x * R, cy - last.y * R)
      ctx.lineTo(cx + (last.x + ex) * R, cy - (last.y + ey) * R)
      ctx.stroke()
      ctx.restore()
    }

    // Solid ISS path across disc
    ctx.beginPath()
    ctx.strokeStyle = transit.type === 'solar' ? 'rgba(0,0,0,0.65)' : 'rgba(30,30,60,0.6)'
    ctx.lineWidth = 2
    ctx.lineCap = 'round'
    ctx.lineJoin = 'round'
    for (let i = 0; i < pts.length; i++) {
      const px = cx + pts[i].x * R
      const py = cy - pts[i].y * R
      if (i === 0) ctx.moveTo(px, py)
      else ctx.lineTo(px, py)
    }
    ctx.stroke()
  }, [transit])

  useEffect(() => { draw() }, [draw])

  return (
    <canvas
      ref={canvasRef}
      width={64} height={64}
      className="iss-transit-disc-icon"
    />
  )
}

function formatSimDate(date) {
  return new Intl.DateTimeFormat(undefined, {
    month: 'short', day: 'numeric', year: 'numeric',
    hour: 'numeric', minute: '2-digit',
  }).format(date)
}

function IssTransitContent({ lat, lng, onTransitPaths, onSelectTransit, onSelectPlace, anchorMode = 'now' }) {
  const { simTime } = useSimTime()
  const [type, setType] = useState('solar')
  const [altInput, setAltInput] = useState('0')
  const [altFetching, setAltFetching] = useState(false)
  const [geoLoading, setGeoLoading] = useState(false)
  const [state, setState] = useState({ loading: false, transits: [], loaded: false, calcTime: null })
  const cancelRef = useRef(null)

  // Reset results when location changes
  useEffect(() => {
    setState({ loading: false, transits: [], loaded: false, calcTime: null })
    onTransitPaths?.(null)
  }, [lat, lng])

  // Auto-fetch elevation from Open-Meteo when lat/lng changes
  useEffect(() => {
    if (lat == null || lng == null) return
    let cancelled = false
    setAltFetching(true)
    fetch(`https://api.open-meteo.com/v1/elevation?latitude=${lat.toFixed(6)}&longitude=${lng.toFixed(6)}`)
      .then(r => r.json())
      .then(d => {
        if (!cancelled && d.elevation?.[0] != null) {
          setAltInput(String(Math.round(Math.max(0, d.elevation[0]))))
        }
      })
      .catch(() => {})
      .finally(() => { if (!cancelled) setAltFetching(false) })
    return () => { cancelled = true }
  }, [lat, lng])

  const handleGetLocation = () => {
    if (!navigator.geolocation || geoLoading) return
    setGeoLoading(true)
    navigator.geolocation.getCurrentPosition(
      pos => {
        setGeoLoading(false)
        onSelectPlace?.(pos.coords.longitude, pos.coords.latitude)
      },
      () => { setGeoLoading(false) },
      { timeout: 8000 }
    )
  }

  const handleTypeChange = (t) => {
    setType(t)
    setState({ loading: false, transits: [], loaded: false })
    onTransitPaths?.(null)
  }

  const handleCalculate = () => {
    if (lat == null || lng == null) return
    cancelRef.current?.()
    const altM = parseFloat(altInput) || 0
    const searchStart = anchorMode === 'sim' ? new Date(simTime) : new Date()
    let cancelled = false
    cancelRef.current = () => { cancelled = true }
    setState({ loading: true, transits: [], loaded: false, calcTime: null })
    onTransitPaths?.(null)
    Promise.all([loadIssTle({ maxCacheMs: DAY_MS }), loadIssArchive()]).then(() => {
      if (cancelled) return
      const opts = { start: searchStart, hoursAhead: 24 * 30, altM }
      const transits = type === 'solar'
        ? findIssSolarTransits(lat, lng, opts)
        : findIssLunarTransits(lat, lng, opts)
      if (cancelled) return
      const paths = {
        centerlines: {
          type: 'FeatureCollection',
          features: transits.filter(tr => tr.path?.length > 1).map(tr => ({
            type: 'Feature',
            geometry: { type: 'LineString', coordinates: tr.path },
            properties: { transitType: tr.type },
          })),
        },
        bands: {
          type: 'FeatureCollection',
          features: transits.filter(tr => tr.band?.length > 3).map(tr => ({
            type: 'Feature',
            geometry: { type: 'Polygon', coordinates: [tr.band] },
            properties: { transitType: tr.type },
          })),
        },
      }
      onTransitPaths?.(paths)
      setState({ loading: false, transits, loaded: true, calcTime: searchStart })
    }).catch(() => {
      if (!cancelled) setState({ loading: false, transits: [], loaded: true, calcTime: searchStart })
    })
  }

  const { loading, transits, loaded, calcTime } = state
  // Stale warning only matters when anchored to the sim clock
  const isStale = anchorMode === 'sim' && loaded && calcTime &&
    Math.abs(simTime.getTime() - calcTime.getTime()) > 6 * 3600 * 1000
  const fmtLat = lat != null ? `${Math.abs(lat).toFixed(4)}° ${lat >= 0 ? 'N' : 'S'}` : '—'
  const fmtLng = lng != null ? `${Math.abs(lng).toFixed(4)}° ${lng >= 0 ? 'E' : 'W'}` : '—'

  return (
    <div className="iss-transit-content">
      {/* Solar / Lunar toggle */}
      <div className="iss-pill-row iss-pill-row--sub">
        <button className={`iss-pill${type === 'solar' ? ' iss-pill--active' : ''}`}
          onClick={() => handleTypeChange('solar')}>Solar</button>
        <button className={`iss-pill${type === 'lunar' ? ' iss-pill--active' : ''}`}
          onClick={() => handleTypeChange('lunar')}>Lunar</button>
      </div>

      {/* Location + altitude */}
      <div className="iss-transit-location">
        <div className="iss-transit-coords">
          <div className="iss-transit-coord-row">
            <span className="iss-transit-coord-label">Lat</span>
            <span className="iss-transit-coord-val">{fmtLat}</span>
          </div>
          <div className="iss-transit-coord-row">
            <span className="iss-transit-coord-label">Lng</span>
            <span className="iss-transit-coord-val">{fmtLng}</span>
          </div>
          <div className="iss-transit-coord-row">
            <span className="iss-transit-coord-label">Alt</span>
            <input
              type="number" min="0" max="8849" step="10"
              className="iss-transit-alt-input"
              value={altInput}
              onChange={e => setAltInput(e.target.value)}
              disabled={altFetching}
            />
            <span className="iss-transit-coord-unit">{altFetching ? '…' : 'm'}</span>
          </div>
        </div>
        <button
          className="iss-transit-geo-btn"
          onClick={handleGetLocation}
          disabled={geoLoading || !navigator.geolocation}
          title="Use your current GPS location"
        >
          {geoLoading ? <><span className="iss-obs-spinner" />{' '}Locating…</> : '⊙ Use my location'}
        </button>
      </div>

      {/* Start time + calculate */}
      <div className="iss-transit-start-row">
        <span className="iss-transit-start-label">From</span>
        <span className="iss-transit-start-val">
          {anchorMode === 'sim' ? formatSimDate(simTime) : `Now · ${formatSimDate(new Date())}`}
        </span>
      </div>
      <button
        className="iss-transit-calc-btn"
        onClick={handleCalculate}
        disabled={loading || lat == null || lng == null}
      >
        {loading
          ? <><span className="iss-obs-spinner" />{' '}Scanning 30 days…</>
          : 'Calculate'}
      </button>

      {isStale && (
        <p className="iss-transit-stale">Time changed — recalculate to update results.</p>
      )}

      {/* Results */}
      {loaded && transits.length === 0 && (
        <p className="iss-obs-empty">No {type} transits found in the 30 days from {formatSimDate(calcTime)}.</p>
      )}
      {loaded && transits.length > 0 && (
        <div className="iss-transit-results">
          {type === 'solar' && <p className="iss-transit-note-obs">Solar filter required to observe safely.</p>}
          <p className="iss-transit-disclaimer">Paths beyond 2 weeks are approximate.</p>
          {transits.map((tr, i) => {
            const beyond = tr.midTime.getTime() > Date.now() + TWO_WEEKS_MS
            return (
              <button
                key={i}
                className={`iss-transit-row${beyond ? ' iss-transit-row--approx' : ''}`}
                onClick={() => onSelectTransit?.(tr)}
                title="Jump to this transit on the map"
              >
                <TransitDiscIcon transit={tr} />
                <div className="iss-transit-left">
                  <span className="iss-obs-date">{formatTransitTime(tr.midTime)}</span>
                  <span className="iss-obs-path">
                    {tr.inBand
                      ? <span className="iss-transit-visible-badge">Visible from here</span>
                      : <span className="iss-transit-nearby-badge">{tr.minDistKm < 1 ? '<1' : Math.round(tr.minDistKm)} km away</span>
                    }
                    {' · '}sep {tr.minSepDeg.toFixed(3)}°
                  </span>
                </div>
                {beyond && <span className="iss-transit-approx-badge">~</span>}
              </button>
            )
          })}
        </div>
      )}
    </div>
  )
}

