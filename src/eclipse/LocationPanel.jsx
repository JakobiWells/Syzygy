// Module-level cache — the PNG is 1440×720 grayscale where each pixel value
// is the Bortle class (1–9) derived from calibrated VIIRS nW/cm²/sr radiance.
// Generated by:  npm run bortle  (scripts/processBortle.py)
let _bortleCtx = null
let _bortleW = 0, _bortleH = 0

async function getBortleCtx() {
  if (_bortleCtx) return _bortleCtx
  const img = new Image()
  await new Promise((res, rej) => {
    img.onload = res
    img.onerror = rej
    img.src = '/bortle_2025.png'
  })
  const canvas = document.createElement('canvas')
  canvas.width = _bortleW = img.width
  canvas.height = _bortleH = img.height
  _bortleCtx = canvas.getContext('2d')
  _bortleCtx.drawImage(img, 0, 0)
  return _bortleCtx
}

function useLocationInfo(lat, lng) {
  const [info, setInfo] = useState({ elevation: null, utcOffsetSec: null, loading: false })

  useEffect(() => {
    if (lat == null || lng == null) return
    setInfo({ elevation: null, utcOffsetSec: null, loading: true })
    let cancelled = false

    Promise.all([
      fetch(`https://api.open-meteo.com/v1/elevation?latitude=${lat.toFixed(6)}&longitude=${lng.toFixed(6)}`).then(r => r.json()),
      fetch(`https://api.open-meteo.com/v1/forecast?latitude=${lat.toFixed(6)}&longitude=${lng.toFixed(6)}&timezone=auto&forecast_days=0`).then(r => r.json()),
    ]).then(([elevData, tzData]) => {
      if (cancelled) return
      setInfo({
        loading: false,
        elevation: elevData.elevation?.[0] != null ? Math.round(elevData.elevation[0]) : null,
        utcOffsetSec: tzData.utc_offset_seconds ?? null,
      })
    }).catch(() => {
      if (!cancelled) setInfo({ elevation: null, utcOffsetSec: null, loading: false })
    })

    return () => { cancelled = true }
  }, [lat, lng])

  return info
}

function useLightPollution(lat, lng) {
  const [state, setState] = useState({ loading: false, bortle: null })

  useEffect(() => {
    if (lat == null || lng == null) return
    setState({ loading: true, bortle: null })
    let cancelled = false

    ;(async () => {
      try {
        const ctx = await getBortleCtx()
        const x = Math.max(0, Math.min(_bortleW - 1, Math.floor((lng + 180) / 360 * _bortleW)))
        const y = Math.max(0, Math.min(_bortleH - 1, Math.floor((90 - lat) / 180 * _bortleH)))
        const [v] = ctx.getImageData(x, y, 1, 1).data
        if (!cancelled) setState({ loading: false, bortle: v > 0 ? v : null })
      } catch {
        if (!cancelled) setState({ loading: false, bortle: null })
      }
    })()

    return () => { cancelled = true }
  }, [lat, lng])

  return state
}

export function IssObservations({ lat, lng, onSelectPass, anchorMode = 'now' }) {
  const { simTime } = useSimTime()
  const [state, setState] = useState({ loading: false, passes: [], tleAge: null, dataSource: null, loaded: false })
  // Anchor: real current time by default; sim clock only when explicitly chosen.
  // Captured once per location so playback ticks don't trigger recomputes.
  const startTimeRef = useRef(anchorMode === 'sim' ? simTime : new Date())

  useEffect(() => {
    startTimeRef.current = anchorMode === 'sim' ? simTime : new Date()
    setState({ loading: false, passes: [], tleAge: null, dataSource: null, loaded: false })
  }, [lat, lng])

  useEffect(() => {
    if (lat == null || lng == null) return
    const startTime = startTimeRef.current
    const nearCurrent = startTime.getTime() >= Date.now() - 7 * DAY_MS
    let cancelled = false
    setState({ loading: true, passes: [], tleAge: null, dataSource: null, loaded: false })
    Promise.all([
      loadIssTle({ maxCacheMs: nearCurrent ? DAY_MS : 6 * 3600 * 1000 }),
      loadIssArchive(),
    ]).then(() => {
      if (cancelled) return
      const passes = findIssVisiblePasses(lat, lng, {
        start: startTime,
        hoursAhead: 24 * 14,
        minElevDeg: 10,
        maxPasses: 80,
      })
      const ds = getIssDataSource(startTime)
      if (cancelled) return
      setState({ loading: false, passes, tleAge: getIssTleAgeDays(startTime), dataSource: ds, loaded: true })
    }).catch(() => {
      if (!cancelled) setState({ loading: false, passes: [], tleAge: null, dataSource: null, loaded: true })
    })
    return () => { cancelled = true }
  }, [lat, lng])

  const { loading, passes, tleAge, dataSource, loaded } = state

  return (
    <div className="iss-obs-body">
      {dataSource && sourceLabel(dataSource.source, dataSource.ageDays) && (
        <p className="iss-obs-warning">{sourceLabel(dataSource.source, dataSource.ageDays)} — positions are estimated.</p>
      )}
      {tleAge != null && tleAge > 3 && dataSource?.source === 'live' && (
        <p className="iss-obs-warning">TLE data is {tleAge.toFixed(1)} days old — pass timing may drift.</p>
      )}

      {loading && (
        <div className="iss-obs-empty">
          <span className="iss-obs-spinner" />
          Calculating passes…
        </div>
      )}
      {loaded && passes.length === 0 && (
        <p className="iss-obs-empty">No visible passes above 10° in the next two weeks.</p>
      )}

      {loaded && passes.length > 0 && (
        <div className="iss-obs-list">
          {passes.map(pass => {
            const mins = Math.floor(pass.durationSec / 60)
            const secs = pass.durationSec % 60
            const score = passScore(pass.maxAlt)
            const color = scoreColor(score)
            return (
              <button
                key={pass.id}
                className="iss-obs-row"
                onClick={() => onSelectPass?.(pass)}
                title="Jump to this pass on the map"
              >
                <div className="iss-obs-left">
                  <span className="iss-obs-datetime">
                    <span className="iss-obs-date">{formatPassDate(pass.start)}</span>
                    <span className="iss-obs-time">{formatPassTime(pass.start)}</span>
                  </span>
                  <span className="iss-obs-path">
                    {pass.startDir} → {pass.maxDir} → {pass.endDir}
                    <span className="iss-obs-dur">{mins}m {String(secs).padStart(2, '0')}s</span>
                  </span>
                </div>
                <div className="iss-obs-right">
                  <span className={`iss-obs-score iss-obs-score--${color}`}>{score}</span>
                  <span className="iss-obs-elev">{pass.maxAlt.toFixed(1)}° max</span>
                </div>
              </button>
            )
          })}
        </div>
      )}
    </div>
  )
}

