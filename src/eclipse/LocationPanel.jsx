function cloudLabel(pct) {
  if (pct === null) return '—'
  if (pct < 20) return `${pct}% (clear)`
  if (pct < 50) return `${pct}% (patchy)`
  return `${pct}% (cloudy)`
}

function formatDuration(s) {
  if (!s) return '—'
  const m = Math.floor(s / 60)
  const sec = s % 60
  return m ? `${m}m ${sec}s` : `${sec}s`
}

function starsFromScore(score) {
  return Math.max(0, Math.min(5, Math.ceil(score / 20)))
}

function formatPassDate(date) {
  return new Intl.DateTimeFormat(undefined, {
    weekday: 'short',
    month: 'short',
    day: 'numeric',
  }).format(date)
}

function formatPassTime(date) {
  return new Intl.DateTimeFormat(undefined, {
    hour: 'numeric',
    minute: '2-digit',
  }).format(date)
}

function formatMag(mag) {
  if (mag == null || !isFinite(mag)) return '—'
  return mag > 0 ? `+${mag.toFixed(1)}` : mag.toFixed(1)
}

// Elevation contributes 70 pts (weather will add up to 30 when integrated)
function passScore(maxAlt) {
  return Math.round(Math.min(maxAlt / 90, 1) * 70)
}

function scoreColor(score) {
  if (score >= 55) return 'high'
  if (score >= 35) return 'mid'
  if (score >= 20) return 'low'
  return 'poor'
}

function sourceLabel(source, ageDays) {
  if (source === 'archive') return `Archive · TLE ±${ageDays != null ? ageDays.toFixed(0) : '?'}d`
  if (source === 'predicted') return `Predicted · ~${ageDays != null ? Math.round(ageDays) : '?'}d from TLE`
  return null
}

